import mongoose from "mongoose"

const MONGODB_URI = process.env.MONGODB_URI

if (!MONGODB_URI) {
  throw new Error("Please define the MONGODB_URI environment variable inside .env.local")
}

/**
 * Global is used here to maintain a cached connection across hot reloads
 * in development. This prevents connections growing exponentially
 * during API Route usage.
 */
let cached = global.mongoose

if (!cached) {
  cached = global.mongoose = { conn: null, promise: null }
}

async function connectDB() {
  console.log("[v0] üîÑ ƒêang k·∫øt n·ªëi MongoDB...")

  if (cached.conn) {
    console.log("[v0] ‚úÖ S·ª≠ d·ª•ng k·∫øt n·ªëi MongoDB ƒë√£ c√≥ s·∫µn")
    return cached.conn
  }

  if (!cached.promise) {
    console.log("[v0] üÜï T·∫°o k·∫øt n·ªëi MongoDB m·ªõi...")
    const opts = {
      bufferCommands: false,
    }

    cached.promise = mongoose.connect(MONGODB_URI!, opts).then((mongoose) => {
      console.log("[v0] ‚úÖ K·∫øt n·ªëi MongoDB th√†nh c√¥ng!")
      return mongoose
    })
  }

  try {
    cached.conn = await cached.promise
    console.log("[v0] üéØ MongoDB ƒë√£ s·∫µn s√†ng s·ª≠ d·ª•ng")
  } catch (e) {
    console.log("[v0] ‚ùå L·ªói k·∫øt n·ªëi MongoDB:", e)
    cached.promise = null
    throw e
  }

  return cached.conn
}

export default connectDB
